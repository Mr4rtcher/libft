#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		h;

	i = 0;
	while (s1[i])
		i++;
	h = 0;
	while (s2[h])
	{
		s1[i + h] = s2[h];
		h++;
	}
	s1[i + h] = '\0';
	return (s1);
}
