#include "libft.h"

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	size_t	i;
	size_t	h;

	i = 0;
	while (s1[i])
		i++;
	h = 0;
	while (s2[h] && h < n)
	{
		s1[i + h] = s2[h];
		h++;
	}
	s1[i + h] = '\0';
	return (s1);
}
