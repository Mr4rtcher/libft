#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;

	i = ft_strlen((char *)s);
	i++;
	while (i--)
		if (s[i] == (char)c)
			return ((char *)s + i);
	return (NULL);
}
